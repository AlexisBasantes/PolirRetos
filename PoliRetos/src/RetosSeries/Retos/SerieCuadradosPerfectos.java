package RetosSeries.Retos;


public class SerieCuadradosPerfectos {
    public static void akMostrarCuadradosPerfectos(int akCantidad) {
        System.out.print("S6: ");
        for (int akI = 1; akI <= akCantidad; akI++) {
            System.out.print((akI * akI) + " ");
        }
        System.out.println("....");
    }
}