package RetosSeries;

public class SerieNumerica07 {
    public static void edSerieNumerica(int i) {
        int valor = 1; 

        for (int contador = 1; contador <= i; contador++) {
            System.out.print(valor + " ");
            valor += 3;
        }

        System.out.println();
    }

   
}

        
    

